function M = meanFilter(I)
  [height, width] = size(I);
  %disp(height);
  %disp(width);
  M = I;
  for row = 2 : height-1
    for col = 2 : width-1
      total = sum([I(row,col+1) ...
          I(row-1,col+1) ...
          I(row-1,col) ...
          I(row-1,col-1) ...
          I(row,col-1) ...
          I(row+1,col-1) ...
          I(row+1,col) ...
          I(row+1,col+1) ...
          I(row,col)]);
      M(row,col) = uint8(total/9);
    endfor
  endfor
end

